public class BgroupDTO {
	
	private int bgroup_id;
	private String bgroup_key;
	private String bgroup_name;
	
	
	public int getBgroup_id() {
		return bgroup_id;
	}
	public void setBgroup_id(int bgroup_id) {
		this.bgroup_id = bgroup_id;
	}
	public String getBgroup_key() {
		return bgroup_key;
	}
	public void setBgroup_key(String bgroup_key) {
		this.bgroup_key = bgroup_key;
	}
	public String getBgroup_name() {
		return bgroup_name;
	}
	public void setBgroup_name(String bgroup_name) {
		this.bgroup_name = bgroup_name;
	}
	
}
